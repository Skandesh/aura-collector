// Helper functions
export const getTasksByCategory = (category: TaskCategory): Task[] => {
  return ALL_TASKS.filter((task) => task.category === category);
};

export const getTasksByDifficulty = (difficulty: string): Task[] => {
  return ALL_TASKS.filter((task) => task.difficulty === difficulty);
};

export const getTaskById = (id: string): Task | undefined => {
  return ALL_TASKS.find((task) => task.id === id);
};

export const getRandomDailyTasks = (count: number = 5): Task[] => {
  const shuffled = [...ALL_TASKS].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};
